package com.test.basket.pricer;

public class ShoppingBasket {
	
    public int jacketCount;
    public int trouserCount;
    public int shirtCount;
    public int tieCount;
    
    public ShoppingBasket() {
    	
    }

	public int getJacketCount() {
		return jacketCount;
	}

	public void setJacketCount(int jacketCount) {
		this.jacketCount = jacketCount;
	}

	public int getTrouserCount() {
		return trouserCount;
	}

	public void setTrouserCount(int trouserCount) {
		this.trouserCount = trouserCount;
	}

	public int getShirtCount() {
		return shirtCount;
	}

	public void setShirtCount(int shirtCount) {
		this.shirtCount = shirtCount;
	}

	public int getTieCount() {
		return tieCount;
	}

	public void setTieCount(int tieCount) {
		this.tieCount = tieCount;
	}
    
    
	
}
